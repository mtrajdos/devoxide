#### Looping through a list

months = ['January', 'February', 'March', 'April', 'May', 'June', 'July', 'August', 'September', 'October', 'November', 'December']

# Using a for loop to print a list
for month in months:
	print(month.title() + ' \n')
	print('The next month is: ')

